# Math Operations

# Addition
result1 = 10 + 5
print("10 + 5 =", result1)

# Subtraction
result2 = 10 - 5
print("10 - 5 =", result2)

# Multiplication
result3 = 10 * 5
print("10 * 5 =", result3)

# Division
result4 = 10 / 5
print("10 / 5 =", result4)

# Power (exponent)
result5 = 2 ** 3  # 2 to the power of 3
print("2 ** 3 =", result5)

# Modulo (remainder)
result6 = 10 % 3  # remainder of 10 divided by 3
print("10 % 3 =", result6)

# Calculate the area of a rectangle
print("\n=== Calculate Area of Rectangle ===")
length = 10
width = 5
area = length * width
print("Area of rectangle:", area)

# Calculate area of a circle
print("\n=== Calculate Area of Circle ===")
import math
radius = 5
circle_area = math.pi * radius ** 2
print("Area of circle with radius", radius, "is", circle_area)
